			<p>It will help that Cathedral returns four of five starters on the offensive line — making line play a real team strength at a school that has recently been better known for its talent at the skill positions.</p>

			<p>“We actually ran out of linemen numbers this year, which is weird for us,” Auger said with a smile.</p>

			<p>“We think our line is pretty far ahead of the game right now. And not just the starting five. We feel like we can play eight or nine guys up there over the course of a game.”</p>

			<p>That depth up front will only assist the Crusaders in their effort to develop more of a balance between the run and pass.</p>

			<p>And Cathedral’s roster is certainly filled with players who are used to taking part in big games.</p>

			<p>Not only did the Crusaders reach the Section 5-3A title game a year ago, but five players on this year’s team were members of both the Cathedral baseball team — which won the Class 2A state title last spring — and the St. Cloud Chutes American Legion team — which recently advanced to the final of the Great Plains Regional.</p>

			<p>“It helps that we have a lot of guys who are used to winning,” said senior Tommy Auger, a two-way starter who had a team-best 131 tackles at linebacker last season and caught 44 passes for 585 yards and seven touchdowns.</p>

			<p>“We haven’t been on many teams growing up that expected to lose games. We’re used to being in pressure situations.”</p>

