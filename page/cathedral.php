			<p>It’s been a while since Jake Spaniol last started a game at quarterback — since the eighth grade, in fact.</p>

			<p>But it is Spaniol — a 6-foot-1, 175-pound senior — who has drawn the task of replacing Jeff Fasching under center at St. Cloud Cathedral this season.</p>

			<p>“I can’t worry about what Jeff did,” said Spaniol of Fasching, the University of Minnesota baseball recruit who threw for 2,773 yards and 31 touchdowns last season while leading the Crusaders to a 6-5 record and the school’s first berth in a section title since 1993.</p>

			<p>“I just have to make sure I’m doing what I need to do to make us a better football team.”</p>

			<p>Spaniol did not play as a freshman or sophomore because of injury and was moved to wide receiver a year ago, though he did see some time at quarterback late in games.</p>

			<p>But Cathedral coach Andy Auger expects he will quickly settle into the starting role.</p>

			<p>“Jake’s a kid who’s always had leadership skills,” said Auger, who begins his second season as the Crusaders’ head coach. “Even going back to when he was young.</p>

			<p>“We’re hopeful he’ll grab the bull by the horns and go with it. We know he understands what we’re trying to do offensively. So we have a lot of confidence in him.”</p>

