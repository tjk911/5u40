			<p>The Little Falls football team has some big shoes to fill with the graduation of running back Jalen Wuchko, who rushed for 1,344 yards and 16 touchdowns a year ago and is playing at Division II Bemidji State.</p>

			<p>But senior Brighton Goodrich — who will be called upon to help fill that void — doesn’t see it as replacing Wuchko so much as moving in a bit different direction.</p>

			<p>“We’ve changed our offensive game plan a lot,” said the 5-foot-11, 175-pound Goodrich, who rushed for 451 yards and two touchdowns.</p>

			<p>“Instead of iso and pounding the ball up the middle, we’ll do more outside tosses and things like that. I’m more of a quick, juke-them kind of running back. I’m not going to run through people the way Jalen did.”</p>

			<p>Despite the loss of Wuchko and Times’ All-Area honorable mention pick Trysten Ross, who also is playing at BSU, Little Falls returns seven starters on each side of the ball from a team that finished 4-5 in 2013 — its best record since 2010. The Flyers boast 22 seniors on this season’s roster — up from 12 a year ago and eight in 2012.</p>

			<p>And Little Falls achieved a big milestone last year by hosting their first playoff game since 1986.</p>

