			<p>“Our seniors last year were very talented, but they never experienced a lot of success and their expectations maybe weren’t as high as this class has,” said Flyers coach Jay DeCann, who begins his third season at the school.</p>

			<p>“They’ve been putting in a lot of time in the weight room this past offseason and they have big goals.” vLittle Falls returns its top two receivers in senior Kyle Kabanuk, who had 19 catches for 372 yards and four touchdowns, and senior Bryce Zawatske, who had 10 catches for 220 yards and five touchdowns.</p>

			<p>At quarterback, senior Austin Fenske is back after throwing for 710 yards and eight TDs. But he is being pushed for the starting job by junior Braden Backowski.</p>

			<p>“I think we’ll probably throw the ball more this year,” Kabanuk said. “We have some guys who are really quick and have good hands.”</p>

			<p>Defensively, three starters are back in the secondary in safeties Goodrich and junior Thomas Miller and senior cornerback Jeremy Klein. Three of four starters return up front, too. And Zawatske returns at middle linebacker.</p>

			<p>“If we come together as a team and we get the kind of leadership I expect we will, I think we can contend for a conference title,” Goodrich said.</p>

			<p>“We had a few games last year that we lost in the fourth quarter. So we feel like we could have been right there. And that’s the goal we’re shooting for this year.”</p>
