			<p>They suggest that they might not have been 100 percent focused and prepared as a group last year. .</p>

			<p>They did a team camp at the University of North Dakota in Grand Forks, to hone their skills and, as a side benefit, improve their bonding. They think all of that will help them turn things around.</p>

			<p>Why? “Ambition,” senior defensive lineman Jack Feldhege said as his teammates laughed.</p>

			<p>Feldhege is sort of the definition of fun. He’s all over the field defensively and will be one of the Spartans’ key linemen.</p>

			<p>Another key will be Budde. He’s quick and fast and though he isn’t listed as a starter on offense in the preseason, count on him being a key player.</p>

			<p>He led the Spartans with 39 receptions last season and should also run the ball and maybe even throw it on occasion when Rocori lines up in some variation of the wildcat offense. He’s being recruited by small Division I and Division II schools and can scoot.</p>

			<p>He ran a 4.78-second 40-yard dash in the Rocori Challenge. It was electronically timed.</p>

			<p>Then, there’s receiver Sam Mackedanz, who should also be a huge factor just because of his athleticism. He has a 36-inch vertical jump. Hansen earned the No. 1 spot on the quarterback depth chart. Skippy Virchow, who started a lot last year, also is in the picture and Budde also is there.</p>

			<p>They’ll have decent size on the line, with left guard Tyler Botten (6-4, 310) the one bringing up its average size.</p>

			<p>Head coach Mike Rowe likes his team and thinks it will compete well.</p>

			<p>And that should be fun.</p>
