			<p>The operative word for Rocori football is fun.</p>

			<p>The Spartans have fun in practice, blasting the sound system with rowdy music. They look like they have a lot of fun in games, running a no-huddle spread with an emphasis on flash and flare.</p>

			<p>“The spread is a fun offense to run,” senior quarterback Tim Hansen said, re-iterating the obvious.</p>

			<p>They seem to have fun on defense, where they rely on quickness and technique to make up for a lack of size.</p>

			<p>“We’re pretty relaxed,” senior tackle Scott Fischer said. “We have a lot of fun.”</p>

			<p>But last year?</p>

			<p>Last year was no fun.</p>

			<p>The Spartans went 1-4 in the Central Lakes South and 3-7 overall. They gave up lots of points, 34.6 per game. They went through quarterbacks like changing bed sheets, about once every couple of weeks because of injuries and just to shake things up.</p>

			<p>“It was rough,” senior defensive back Alex Budde said.</p>

			<p>“I don’t want a season like last year to be the way we go out,” senior Victor Schleppenbach said.</p>

			<p>The upside is they played lots of guys and many of them return.</p>

			<p>“We’ve got a lot experience this year,” senior tackle Scott Fischer said. “It’s like we played a lot to prepare for this year.”</p>

			<p>Experience is nice, but they’ve also worked on their attitude and preparation.</p>

