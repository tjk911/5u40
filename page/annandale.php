			<p>Junior Brock Fobbe (5-11, 155) will move from safety, where he started as a sophomore, and take the reins at quarterback. Fobbe replaces Riley Atkinson, who threw for 2,128 yards and 20 touchdowns as a senior last season.</p>

			<p>“(Fobbe) is coming along fine, he’s looked good. He’s a little bit smaller than what we’ve had in the past, so we’re going to try to get him out of the pocket a little bit more and let him be an athlete,” Walter said.</p>

			<p>Ogden, Kalla and Jake Laudenbach will be the linebacking corps, and senior cornerback Spencer Haag returns on a defense that coach Walter thinks will be improved from last season.</p>

			<p>“They (Fournier, Laudenbach and Ogden) saw significant time on defense last year,” Walter said. “We’re small at linebacker, but I think we’re quick there.</p>

			<p>“Austin Kalla is going to be a good football player for us; he went down with a knee injury in week six last year, but he’s back 100 percent.”</p>

			<p>Haag and his teammates are drawing on the experience they gained over the past few seasons to help combat some of the turnovers coming into this year.</p>

			<p>“Just seeing the enthusiasm all those guys had on the field helped a lot, and it shows that even a little town like ours can make it if you put in the work,” Haag said.</p>

			<p>The Cardinals will face some strenuous opposition early in their schedule. Conference matchups against Hutchinson and Orono in the first two weeks of the season will be a good test.</p>

			<p>Hutchinson capped off an undefeated season (13-0), with a 49-7 rout over Holy Angels to capture the Class 4A state title last season.</p>

			<p>Orono (7-3) reached the Section 5- 4A semifinals where the Orioles were edged by Becker 28-26.</p>

			<p>“We play a lot of big teams with big lines, so we need to be physical with them and bring that to the postseason,” Kalla said.</p>

