			<p>There has been a constant goal for Matt Walter’s Cardinals over his 14 years as head coach: Get to the section championship game and win it.</p>

			<p>“Our goal is to be playing our best football at the end of the year and to try to make a run in the sections. If we win enough games and they want to hand us a trophy at the end of the (conference) year, we’ll take it, but that’s not what we’re shooting for,” Walter said.</p>

			<p>Annandale has made that goal a reality in recent years by reaching the section championship the past three seasons, coming away with two victories. The Cardinals fell in a 66-46 shootout to the eventual Class 3A state runner-up and conference foe New London-Spicer in last season’s final.</p>

			<p>The loss ended the Cardinals’ season with an overall record of 7-4. Those four losses were all to Wright County Conference squads (Glencoe-Silver Lake, NL-S, Hutchinson and Delano), who all advanced to the state tournament.</p>

			<p>The Cardinals have their work cut out for them to reach the section championship this season. They return just one starter on offense: running back Spencer Ogden (6-foot-2, 210 pounds), who could get the bulk of the carries.</p>

			<p>Ogden rushed for 324 yards and two touchdowns last season and was a participant in the state wrestling tournament at 195 pounds.</p>

			<p>“I feel like people are going to fill in a lot better as we go along,” Ogden said of the new faces on offense. “We don’t have that (many) big linemen so we’re not going to be able to power drive. We’re going to just have to use our speed.”</p>

			<p>Ogden, who would like to play college football, has been in contact with Upper Iowa and St. Cloud State. Joining Ogden in the backfield will be senior Austin Kalla (5-10, 180 pounds), and junior Brett Wadman (5-8, 160).</p>

