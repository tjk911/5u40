			<p>There’s a new head coach in Milaca this season.</p>

			<p>Senior wide receiver Nic Wolbert and his teammates are hoping it marks the start of a new era for the Wolves as well.</p>

			<p>“I think we all just have a better attitude and a better outlook on things,” said Wolbert, whose team finished 0-9 his sophomore season and 2-7 last year.</p>

			<p>“It feels like a new start and we know we have some good things going for us.”</p>

			<p>One of those things is Wolbert, a 6-foot-3, 210-pounder who caught a team-best 28 catches for 424 yards and three touchdowns a year ago.</p>

			<p>And longtime assistant Cory Anderson — who takes over the head coaching job from Randy Johnson — is happy to have him back in the fold.</p>

			<p>“A guy like that just gives everybody on the team more confidence,” said Johnson, who spent the past 16 seasons as an assistant coach at the school. “Our nickname for him is Megatron because (like Detroit’s Calvin Johnson) he’s so big and strong.</p>

			<p>“He had a great summer in 7-on-7 and we expect big things from him.”</p>

			<p>Who will get him the ball is yet to be determined.</p>

