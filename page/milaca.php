			<p>Senior Michael Garber was playing quarterback to start last season, but broke a finger in the first game and played wide receiver — as well as starting at safety — when he returned.</p>

			<p>Junior Nate Hass took over the starting job and threw for 1,139 yards and nine touchdowns.</p>

			<p>The two have been competing for the job this preseason. But Garber is likely to end up at wide receiver.</p>

			<p>Six offensive starters are back, including Wolbert and senior Christian Kruse in the receiving corps (though he may play wing back), senior Zach Peterson and Josh Otten at tackle and classmate Brad Bekius at guard.</p>

			<p>“Teamwork is going to be the key this season,” Kruse said. “It takes teamwork to make the dream work.</p>

			<p>“We have a lot of guys back and everybody wants to be here. We’re going to leave everything out on the field.”</p>

			<p>Defensively, six starters are back as well: Otten, senior Austin Weidner and junior Ted Blenkush at linebackers, Garber in the secondary and senior Colton Sailor at defensive end.</p>

			<p>“We know what to do to be successful,” Sailor said. “We just have to be more consistent.</p>

			<p>“We have a lot of guys who have been through all this now. We played the games. We knew what to expect. So we’re a lot more confident as a team.”</p>
