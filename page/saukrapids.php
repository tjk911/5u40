			<p>Come off a rough season and expect a memorable slogan from head coach Bill Magnuson and the Sauk Rapids Storm.</p>

			<p>Heck, even coming off great seasons like 2008, ’09 and ’10, for Magnuson and his staff, there were slogans suitable for printing on black, green and yellow t-shirts.</p>

			<p>The one for 2014 is all about the GRIND.</p>

			<p>That’s GRIND as in: Get Ready. It’s a New Day.</p>

			<p>The Storm are coming off an 0-9 season that had its share of bad breaks.</p>

			<p>They lost 35-34 to Fergus Falls, failing to convert on a two-point PAT after a late touchdown.</p>

			<p>There was a 21-14 loss to St. Cloud Tech and a 16-15 defeat to Rocori, all winnable games in the Storm’s eyes.</p>

			<p>The goal is to turn that round.</p>

			<p>“It’s a clean season,” senior center Andrew Vannurden said.</p>

			<p>“A totally new season,” senior quarterback Jeremy Thorson said.</p>

			<p>In the process, they’d like to break at 13-game losing streak and a stretch where they’ve lost 17 of 18 games. The only break was a 20-12 win Sept. 29, 2012, at Rocori.</p>

			<p>Sauk Rapids hasn’t won a home game since Sept. 9, 2011, a 25-22 win over Sartell.</p>

			<p>That’s a lot of losing for a program that qualified for the state playoffs in 2010, went 9-2 in 2008 and hadn’t had a losing season in five years before the current skid.</p>

			<p>To do it, they’re going to need the usual stuff.</p>

			<p>“Typical of what any coach says, we’ve got to stay healthy,” Magnuson said.</p>

			<p>“We’ve got some decent players. Depth is an issue.”</p>

