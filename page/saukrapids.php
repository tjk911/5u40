			<p>Leading return rusher Bobby Witoski had foot surgery after a summer football injury and is done for the season. Mitch Waldorf, a possible starter at wide receiver and cornerback, hurt his shoulder over the summer. He may return by midseason.</p>

			<p>“It’s really going to be about discipline for us,” Magnuson said. “Discipline is the key to our success.</p>

			<p>“We have to be better disciplined and be able to do what are job is. And, we have to have a few breaks along the way.”</p>

			<p>The core indeed is strong.</p>

			<p>Fullback/linebacker Barclay Bates is one of the better players in the Central Lakes Conference. Thorson replaces Tyler Bjork, who spearheaded one of the area’s top passing games. Bjork’s now playing baseball at St. Cloud State.</p>

			<p>And, there’s some tough kids on the line.</p>

			<p>“It’s definitely been competitive for some spots,” Vannurden said. “It keeps practice interesting.”</p>

			<p>Vannurden and fellow lineman and captain Cole Boughton both are farm boys. Boughton works on a 400-500 acre family farm with 70 milking cows. Vannurden is on a 700-800 acre family farm with 100 cows.</p>

			<p>“It’s good to have them,” Thorson said. “They know how to work.”</p>

			<p>Magnuson really likes his main players. Most are part of a group of 18 seniors. He’s trying to get most of the rest of the squad to jump on board.</p>

			<p>“It is a challenge for the general population,” Magnuson said. “For the core, the hard-cores, it is not.</p>

			<p>“They want to be successful. The problem is that core is 8-10 people. With the others, with hard work,we can change some of those bad things from happening.”</p>

			<p>If that comes together, the Storm think they can surprise people.</p>

			<p>“We’re probably scheduled for half the homecomings this year,” Thorson said. “Let’s spoil some of those.”</p>
