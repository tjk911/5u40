			<p>Perhaps the Holdingford football team should have shirts made up for their theme this season.</p>

			<p>“Raise the bar, that’s the motto,” senior Matt Messman said.</p>

			<p>The bar has been set pretty high during the first two seasons that Luke Mitchell has been the Huskers’ head coach.</p>

			<p>Holdingford is 14-0 in Central Minnesota Conference games, 21-3 overall and made two straight Class 2A state quarterfinals appearances. The Huskers have lost back-to-back quarterfinals games by a combined total of 10 points, keeping them from reaching the semifinals, which have been played in the Metrodome.</p>

			<p>“It’s heart-wrenching, knowing that you were that close to being in the Dome,” Messman said. “The goals are the same this year as last year ... Anything less would kind of be a disappointment.” v“We always went into games thinking that we were going to win because we had a pretty good team,” said senior Nathan Brinker, who is beginning his fourth varsity season. “That confidence boost really carries over.”</p>

			<p>Linebackers Brinker (75 tackles, 14 for loss, 5 sacks, 3 blocked kicks), Messman (73 tackles, 4 1/2 for loss) and senior Jake Knettel (46 tackles, 3 1/2 for loss, 2 1/2 sacks) are the three returning starters on defense and were the team’s top three tacklers last season.</p>

			<p>“Our linebacker core is going to be just as good as last year,” Messman said.</p>

