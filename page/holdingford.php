			<p>Holdingford held opponents to an average of 9.9 points and 208 1/2 yards of total offense last season. The Huskers averaged more than six tackles for loss and more than two sacks per game.</p>

			<p>“If we can play great defense and play well on special teams, the other stuff is going to take care of itself,” Mitchell said. “If we can win that battle of field position, I think that’s so hard to play on a short field.”</p>

			<p>Offensively, the Huskers have three starters back, but all three are getting recruiting looks from NCAA Division II and III programs. Senior right tackle Jake Langner, Brinker (right guard) and Messman are the returners. Messman is moving from fullback to tailback this season.</p>

			<p>Holdingford has a sizable hole to fill at quarterback, a position that Mitch Messman (Matt’s brother) started at for four years. He was the Times Area Player of the Year after having 2,790 total offensive yards with 23 TD passes and 17 rushing TDs.</p>

			<p>“Obviously, it’s going to be different,” Matt Messman said of his brother moving on to play at St. John’s “But our new quarterback, Austin (Gerads), was behind Mitch for three years and he’s ready to play.</p>

			<p>“He’s going to do great this year. I think he’s just going to be himself and surprise people.”</p>
