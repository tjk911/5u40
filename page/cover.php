			<p>For as long as anyone can remember, the preseason goals for most high school football programs were the same. And they usually started with winning a conference title.</p>

			<p>That was job one and it will soon be out the window. This season will be the last time anyone can win a conference title in Minnesota for the foreseeable future.</p>

			<p>Conferences are out after 2014. Districts are in.</p>

			<p>That was a decision made by the Minnesota State High School League last May.</p>

			<div class="large-4 medium-4 column right panel" style="margin-left:10px;">
			  <img src="assets/cover/cover4.jpg">
			  <h5><b>St. Cloud Technical High School football players take the field Friday to play Apollo High School at Michie Field in St. Cloud.</b></h5>
			  <h5 class="caption">(Photo: Jason Wachter, jwachter@stcloudtimes.com)</h5>
			</div>

			<p>What it means to the average fan is this is the last time (your school here) competes for conference hardware and (your favorite player’s name here) can earn all-conference honors.</p>

			<p>In its place will be something that does a few good things.</p>

			<p>Districts have been set up around the state and include somewhere between 12-28 teams. And while teams placed in districts will be similar in size, they won’t necessarily be in the same class.</p>

			<h3>The good stuff:</h3>

			<ol>
				<li>Everyone everywhere will have a full, eight-game schedule played roughly on the same dates in the same week. Zero-week scheduling won’t happen. You won’t have to travel crazy distances, leave the state or — in the case of Eden Prairie a few years ago — leave the country.</li>

				<li>For the most part, local rivalries will remain and postseason playoff sections will not change. Playoff sections will still be aligned by computer with oversight by the high school league.</li>
			</ol>

			<p>Districts will be scheduled by administrators much like conferences are now. In fact, a lot of district schedules are going to look an awful lot like conference schedules now.</p>

			<h3>Coming in 2015</h3>

			<p>Area administrators have worked over the summer to put together schedules for the 2015 and ’16. Area coaches are bracing themselves for changes, but for the most part, everybody is on board and looking forward to it.</p>

			<p>Coaches in the Central Lakes Conference went to a two-division format last season that seemed to solve all the issues that plagued the CLC for the past decade.</p>

			<p>Everyone in each division plays a similar schedule with the same number of games, something that didn’t happen until last year. A team could win the conference title without playing one of the top teams, which happened several times.</p>

			<p>“It’ll be interesting to see how everything plays out once the high school league gets through with it,” Tech head coach Gregg Martig said of district scheduling. “Last year, turned out to be a nice arrangement (in the Central Lakes).”</p>

			<p>The CLC’s issues were a microcosm of what was going on around the state and what districts should help minimize.</p>

			<p>“The conference thing for me is we finally had a really good system in place,” Apollo head coach Justin Skaalerud said. “I understand why this all went into place. I thought it (the 2013 Central Lakes divisions) went really well.</p>

			<p>“Fortunately, we found out that it’s going to be very similar to the sub-districts. It might end up being a semantics thing.”</p>

