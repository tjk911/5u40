			<h3>Some change</h3>

			<p>District scheduling will be a little bit different for everyone.</p>

			<p>For Central Lakes teams, the most noticeable absence will be Fergus Falls.</p>

			<p>In the Granite Ridge Conference, Mora is headed north and east. Likely replacing the Mustangs will be New London-Spicer, a Prep Bowl team last season and a familiar foe to Granite Ridge teams like Albany and St. Cloud Cathedral.</p>

			<p>Crusaders head coach Andy Auger, for one, isn’t too concerned about the conference stuff.</p>

			<p>“For us, since I’ve been around, it’s never been about that,” Auger said. “We’re more about the section playoffs. ...</p>

			<p>“It’s nice to be all-conference and win a conference championship, but it’s never been a huge priority for our program.”</p>

			<div class="large-4 medium-4 column left panel" style="margin-right:20px;">
			  <img src="assets/cover/cover5.jpg">
			  <h5><b>Apollo fans cheer as their team takes the field to play Tech at Michie Field last year.</b></h5>
			  <h5 class="caption">(Photo: Jason Wachter, jwachter@stcloudtimes.com)</h5>
			</div>

			<p>For Central Minnesota Conference teams, there will be some changes. The conference’s smaller schools, like Kimball and Belgrade-Brooten-Elrosa, are going to see some changes. Both are Class A teams.</p>

			<p>“We’re going to drive a lot more,” Kimball head coach Joe Brennan said. “It’s a tough conference we play in. But it prepares us for the playoffs, which will be good.</p>

			<p>“There are still some real good teams in our district. We’re going to benefit in playing similarly sized schools.”</p>

			<p>The Cubs’ new district includes Prairie Valley (which is Parkers Prairie and Eagle Valley), Kerkhoven-Murdock-Sunburg, Royalton, Upsala/Swanville Area, West Central, Belgrade-Brooten-Elrosa and Browerville.</p>

			<p>No longer will Kimball play Class 3A schools like Pierz and Rockford. Brennan said he hopes the Cubs will be able to maintain their rivalry game with Eden Valley-Watkins in the future. Kimball and EV-W play annually in the Rooster Bowl with a traveling trophy going to the winner.</p>

			<p>“I think it (district scheduling) is going to benefit us tremendously,” Brennan said. “We’re going to play schools our size and that, hopefully, will get the ball rolling for us as a program.”</p>

			<p>That, of course, is the goal for everybody. Most coaches believe the new district system will be more fair competitively and travel-wise.</p>

			<p>It also could be changed. It will be re-assessed and evaluated.</p>

			<p>“I’m not seeing it as something that will affect us a great deal,” Sartell head coach Scott Hentges said.</p>

			<p>In Sartell’s case, it just means that once or twice a season, the bus will point in a different direction for a long drive. Instead of Fergus Falls, for instance, the Sabres are likely headed to Grand Rapids.</p>

			<p>It’s still up in the air whether districts will hand out all-district awards. It won’t be all-conference, though.</p>

			<p>But like Skaalerud pointed out, it’s more semantics.</p>

			<p>“Some things are still up in the air,” he said.</p>

