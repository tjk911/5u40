			<p>“We want to have three to four guys who can run the ball,” Noll said. “We’ve got a young offensive line with all juniors. We’ll be a work in progress. Zack’s a hockey player by trade. He’ll run you over. That’s all right with us.”</p>

			<p>Senior Matthew Beste returns at quarterback. He threw for 922 yards and seven TDs last season, but threw 11 interceptions. His main target will be 6-3, 190-pound wide receiver Blake Gerads, who had 21 catches for 340 yards and four TDs last fall.</p>

			<p>Melrose’s biggest loss from last season might be the 6-foot-6, 300-pound Dallas Rieland along the offensive and defensive lines. Rieland. a state all-star, is playing at Division III Concordia-Moorhead. Replacing him won’t be easy.</p>

			<p>“His presence certainly helped,” Glasener said. “He would take up a lot of double teams and that helped our linebackers.”</p>

			<p>Melrose returns Gerads and Colton Meyer (6-2, 210) along the defensive line, Pierskalla and Welle at linebacker and Isaac Herkenhoff and Beste in the secondary.</p>

			<p>“Colton and Blake have played a lot of snaps for us,” Glasener said. “Welle, Gerads, Herkenhoff and Beste are all three-year starters for us and that’s invaluable. That gives us a lot of hope.</p>

			<p>“If our front line can hang in there, I think we have some potential.”</p>
