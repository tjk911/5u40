			<p>The Melrose football team was on the road for every game last season.</p>

			<p>The Dutchmen played their home games at Clemens Stadium at St. John’s while their football field underwent extensive renovations. It now has brand new sod in place.</p>

			<p>Despite being away from home, Melrose still had a strong season, going 6-4 overall and finishing 5-3 in the West Central Conference.</p>

			<p>“We’re just happy we don’t have to take a bus to every game,” Dutchmen senior fullback/linebacker Anthony Welle said.</p>

			<p>Melrose co-head coaches Chuck Noll and Vaughn Glasener are excited about the new playing surface. Some of the final touches were being put on the outside of the stadium during the team’s first week of practice.</p>

			<p>“The older I get, the more I hate bus rides,” Noll said with a laugh. “I even hated the ones down to St. John’s. It was nice playing there, but our facilities are second-to-none here. The grass is beautiful and everything is so new. It’s nice to be home.”</p>

			<p>The Dutchmen return 5-foot-10, 200-pound junior Zack Pierskalla at running back. He ran for 993 yards and 11 touchdowns last season. Noll, who runs the offense, wants to have a more balanced attack this season, but knows Pierskalla is the team’s main threat.</p>

