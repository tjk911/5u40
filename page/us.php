			<p>For the Upsala/Swanville Area Patriots, this season has one main goal: Make it back to the state tournament.</p>

			<p>After finishing the 2013 season with a record of 4-1 in the Prairie Conference and 10-2 overall, as well as reaching the Class A state tournament, senior Tyler Evans thinks that this year’s Upsala/Swanville Area team has enough experience and talent to return.</p>

			<p>“We bring back quite a bit of experience to this year’s squad,” said the 5-foot-10, 160-pound quarterback. “I think we have a good chance at being a successful team if we put in the work.”</p>

			<p>One question mark heading into the year is who would replace the graduated Mitch Lange, who ran for 3,473 yards and scored 48 touchdowns in his junior and senior seasons combined.</p>

			<p>Head coach Jay Loven expects Mitch’s younger brother Kyle to step into his spot and help the offense flourish.</p>

			<p>“Even though we don’t have Mitch, we expect the same from our offense this year. And we believe that his brother Kyle will fill in for him well.”</p>

			<p>Kyle, a 6-foot-1, 190-pound junior running back/linebacker, has high hopes for this year’s team.</p>

			<p>“Our expectations are still high. We have a few guys to replace, but we still have most of our team returning,” he said.</p>

			<p>“We really want to make it to state and win it this year.”</p>

