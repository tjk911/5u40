			<p>Although the players have their eyes on going deep in the postseason, Loven — who is entering his 11th season at the helm — doesn’t like expressing his expectations and wants to make sure his team takes it game-by-game in the season ahead.</p>

			<p>“Last year is last year. That’s in the past now. I’ve seen many teams make it to the state tournament and come back with the same team the next year, but don’t prepare themselves right,” he said. “Although we have our goals, we don’t like putting any expectations on our players, because I believe that puts too much heat on them.</p>

			<p>“We are going to take this year one game at a time.”</p>

			<p>USA, which lost to the 2013 Class A state champion Mahnomen 28-12 in the first round of the state tournament, will have 15 of the 22 starters returning to the team.</p>

			<p>Even with the first-round loss at state, Loven believes that the experience his team gained will help them tremendously.</p>

			<p>“We’ve been in the section championship game two or three other times, without being able to break that ceiling and advancing farther.” he said. “I think last year will really benefit us because the kids now know what it takes to go deep in the playoffs and how hard it is to reach state. With having 15 of our starters back, I think all of us know what it will take to get back.”</p>

			<p>“A lot of hard work will have to be put in and we can’t take anything for granted. We have to work hard in order to get back to where we were,” Evans said.</p>

			<p>If the Upsala/Swanville Area Patriots do just that, Evans’ goals — in his eyes — are rather simple.</p>

			<p>“We want to win the conference, win the state championship and go an undefeated 14-0.”</p>
