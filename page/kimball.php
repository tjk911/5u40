			<p>“You can definitely tell who went to the summer camp and who didn’t,” senior fulback/middle linebacker Troy Filzen said. “The people who did are way better conditioned. They’re ready to go and know the schemes.”</p>

			<p>The summer time together gave the Cubs a chance to implement a new offense and defense.</p>

			<p>“Our offense is based on the pass,” junior slot back Brady Mehr said. “We always have four receivers, but we will do some pro power sets.”</p>

			<p>Kimball will go with a base defensive scheme that has five defensive linemen and three linebackers.</p>

			<p>“They made their schemes for how we are,” Filzen said of the coaching staff. “We don’t try to do something that we’re not.”</p>

			<p>What the Cubs do not want to do is repeat last season. Kimball was 0-7 in the Central Minnesota Conference and 0-9 overall after finishing 5-5 in 2012.</p>

			<p>“Anything can be better than last year,” senior tight end/defensive end Jarred Merchant said. “We’re trying to get this program back on the right track.”</p>

			<p>Brennan has liked what he has seen in the preseason.</p>

			<p>“I’ve been preaching ‘attack the day.’ We’re going to go out and work hard and get better each day,” he said. “We want to improve off last year and I think we’re on the right path with the effort they’re putting in.</p>

			<p>“All the extra things they’ve been putting in are going to help them.”</p>
