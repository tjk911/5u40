			<p>Joe Brennan tries to make laughter a part of every practice.</p>

			<p>The second week of practice, for example, Brennan had all of the Kimball players who want a chance to kick gather.</p>

			<p>A group of eight players gathered and then Brennan and assistant coach Jason Mortenson stood on either side of the kickers as they attempted a field goal.</p>

			<p>The deal was that the number of misses would equal the number of sprints the Cubs had to do and Brennan and Mortenson tried to say something funny to each competitor as they were kicking.</p>

			<p>“I’m really glad he came here,” senior Tyler Smith said of Brennan, who was hired last spring. “It’s pretty upbeat. Practices are always fun and we’re enjoying ourselves the whole time.”</p>

			<p>This is Brennan’s first season as a head coach after coaching stops at St. Cloud Apollo, Big Lake, Dassel-Cokato and Kennedy Community School.</p>

			<p>“You’ve got to have a work mentality, but it’s got to be kind of a light, focused atmosphere,” said Brennan, a fifth-grade teacher. “When we’re at work, we work. When we have a little break, we inject a little humor to keep them there and buy into it.”</p>

			<p>Kimball has 37 players out for football in grades 9-12, but Brennan got about 25 of them to buy into attending a team strength and speed camp three times a week during the summer.</p>

