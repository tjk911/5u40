			<p>Dwight Lundeen has been coaching in Becker a long time, and he’s usually operated by a simple rule.</p>

			<p>You choose one starting quarterback.</p>

			<p>That changed last year when Lundeen and the Bulldogs went into the season playing three quarterbacks — then-sophomore Andrew Stanger, then-junior Michael Veldman and then-senior Zach Zimmer.</p>

			<p>An injury early on took Zimmer out of the rotation. But Veldman and Stanger continued to rotate — a system Lundeen expects to continue this year.</p>

			<p>“I’d never done three before and I’ve never done two,” said Lundeen, who begins his 45th season at Becker with 317 career victories. “But I think we’re going to do two this year because they’re both very good.”</p>

			<p>Veldman, who threw for 865 yards and 13 touchdowns as Becker repeated as Granite Ridge Conference champions in 2013, said the two-quarterback approach is OK with him.</p>

			<p>“We’re both good friends and we both bring different aspects to the game,” Veldman said. “And we both want what’s best for the team.”</p>

