			<p>“It was a little tough at first, just trying to get into a rhythm going in and out,” said Stanger, who threw for 507 yards and seven touchdowns as his team finished 8-2 and fell 20-14 to Delano in the Section 5-4A title game. “But we work well together. He’s more of a pocket passer and I’m more of a runner and scrambler.”</p>

			<p>Whomever is under center will be running an offense that returns some talented weapons. Senior wide receiver Eric Blomgren is back after catching 23 passes for 421 yards and five touchdowns a year ago. Senior Matt Conzemius had 18 catches for 248 yards and seven touchdowns. And junior Robbie Benson had 16 catches for 250 yards and four touchdowns. Senior C.J. Schwintek carried the ball 60 times for 442 yards and two touchdowns.</p>

			<p>And three starters are back on the offensive line, led by junior tackle Casey Vesledahl, the offensive lineman of the year in the Granite Ridge Conference in 2013, and senior guard Chris Weberg, who — like Veldman and Stanger — was an all-conference second-team pick. But Vesledahl will miss at least six weeks after a preseason knee injury.</p>

			<p>“We have some really good skill position kids coming back,” Lundeen said. “They were high-impact kids last year and they’ve gotten bigger, stronger and faster.”</p>

			<p>Defensively, seven starters return, led by senior first-team all-conference pick Dawson Brown at safety. Second-team all-conference picks Beau Pauly and Milan Kent are back in the linebacking corps.</p>

			<p>“Our goal is to win a state championship,” said Stanger, the son of former Becker and St. John’s quarterback Corey Stanger, who also returns as a starter at cornerback.</p>

