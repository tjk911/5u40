			<p>The Huskies still don’t have an overpowering offensive line, but will continue to pound the ball on the ground. After all, it is Albany tradition.</p>

			<p>“We always take a lot of pride in running the ball,” Olmscheid said. “We like our smash-mouth football. Everybody knows what we’re going to do, but they still have to stop it.”</p>

			<p>The offensive line returns junior Danny Odenthal and seniors Tyler Birr and Jordan Kittleson. Tight end Kenny Weber also is back.</p>

			<p>“We’ve gotten a little bulkier over the past year,” Tomsche said. “We have quite a few guys back with experience. It’s nice having all of them back.”</p>

			<p>Tomsche could share time at quarterback with Fourre. Fourre took over for Tomsche midway through last season when a concussion ended Tomsche’s season. Tomsche could get time in the backfield and at wide receiver during the season.</p>

			<p>Albany’s defensive secondary was burned badly at times last season. But the Huskies return eight players on the defensive side that could help slow down aerial attacks this season.</p>

			<p>“Teams threw against us and had some success,” Kleinschmidt said. “Our kids are working hard there to get better. We’re an aggressive team that has worked hard in the secondary. It’s been a point of emphasis in camp.</p>

			<p>“Other teams have raised the bar with their passing games and we have to catch up.”</p>

