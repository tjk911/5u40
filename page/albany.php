			<p>Playing at the Class 4A level was an eye-opener for Albany last season.</p>

			<p>The Huskies finished with their first losing season since 1994 in a 4-6 campaign. It was the first losing year for 12th-year head coach Mike Kleinschmidt.</p>

			<p>“I found out there is a fine line between winning and losing,” Kleinschmidt said. “I was always taught that by the head coaches here before me. Last year we happened to be on the wrong side of the line. We’re trying to amend that. We want to get back on that winning side this year.”</p>

			<p>Things don’t get any easier for the Huskies this season. The Granite Ridge Conference has proven to be tough, with the likes of Becker, Foley and St. Cloud Cathedral.</p>

			<p>“We found out that Class 4A football is more challenging last year,” senior running back Nick Mergen said. “But I think we’ll rise to the challenge this season. We took our lumps, but we’re ready for the competition this year.”</p>

			<p>Albany’s offense will be built around junior quarterback Taylor Fourre, quarterback/split end Zac Tomsche, Mergen and senior running back Shane Olmscheid. Mergen was the Huskies’ leading rusher last season with 580 yards on 89 carries and two touchdowns.</p>

