			<p>The sight of an athletic 6-foot-5, 230-pound football player walking down a high school hall can be intimidating.</p>

			<p>When senior Jared Streit walks down the halls of Eden Valley-Watkins High School, though, there’s normally a positive reaction. If he is walking around after school days end, he is one of three students who are talking to teachers and seeing if they need any help or work done on their computers.</p>

			<p>“We update computers, clean them, change them out,” Streit said of his IT assistance. “Whatever the teachers need.”</p>

			<p>Streit’s also on the student council, in the investment club and in a leadership class.</p>

			<p>“He’s always helping the younger kids,” senior Cody Schlangen said. “He’s always trying to help the team.”</p>

			<p>If that sounds like the characteristics of an oldest child, you’ve picked up on something. Streit has three younger brothers: Jacob is a sophomore, Justin is an eighth-grader and Josh is a sixth-grader.</p>

			<p>“I try to treat everybody the same,” Streit said. “Just because I’m older doesn’t mean that I’m right about everything.”</p>

			<p>Streit has been getting a lot of interest from college football recruiters. Last season, he started at tight end, defensive end and kicker. He had 26 receptions and 11 sacks and then took part in summer football camps at North Dakota State and the University of Minnesota.</p>

