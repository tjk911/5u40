			<p>North Dakota State, South Dakota State, North Dakota and Drake are among his NCAA Division I suitors. He’s also been a varsity basketball player since the eighth grade and has talked to St. John’s about playing both sports there.</p>

			<p>“He kind of emerged last season in football,” coach Jon Thielen said. “But in basketball, he started taking off and people started to recognize his athleticism.”</p>

			<p>Added Thielen: “He’s earned everything he’s getting because he’s in the weight room a lot and by going to camps.”</p>

			<p>He ran the 40-yard dash in 4.9 seconds at the North Dakota State camp and in 4.8 at the start of fall practice.</p>

			<p>He is one of six starters back on offense and one of three returning linemen. The Eagles went into camp looking for a starting quarterback. Junior Travis Thielen and sophomore Reese Jansen, who both played wide receiver and defensive back last season, battled for the starting position.</p>

			<p>“They’re not real big, but athletic,” Jon Thielen said of the quarterbacks. “They’re probably the most athletic quarterbacks we’ve had in a little while.</p>

			<p>“They can do a lot of different things, running and throwing.”</p>

			<p>The Eagles have seven starters back on defense. But there are 42 players in grades 9-12, so the hope is that the experienced players stay healthy.</p>
