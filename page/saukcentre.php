			<p>This year’s Sauk Centre quarterback was a wide receiver last season.</p>

			<p>Matthew Moritz caught 11 passes for 129 yards last fall from quarterback Seth Otte. This year he’s getting his chance to lead the Mainstreeters.</p>

			<p>“The transition back to quarterback has gone well for me,” Moritz said. “I still practiced last year with Seth and I moved out to wide receiver so I could get playing time.</p>

			<p>“I liked receiver last year, but I’m glad to be back at quarterback.”</p>

			<p>The Mainstreeters return the 6-foot-1, 190-pound Austin Nelson behind Moritz. Nelson had 76 carries for 500 yards and three touchdowns last season.</p>

			<p>Nelson also had 14 catches for 225 yards and two scores.</p>

			<p>“Austin stepped up toward the end of last year and got a lot of carries for us,” fifth-year Sauk Centre head coach Charlie Warring said. “He’s going to play a lot at tailback this fall. He’ll have to carry the load for us.”</p>

			<p>Sauk Centre returns a strong nucleus along the offensive line in senior tackles Charlie Barhorst and Lucas Chalupnik while Cole Middendorf, Danny Schmeising and Nick Wanderscheid all have experience.</p>

