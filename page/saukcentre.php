			<p>The ’Streeters should be able to push some people around with a line that has most of its players over 200 pounds.</p>

			<p>“We spent a lot of time in the weight room this summer,” Barhorst said. “We have a lot of big guys along the line. We should have a great push off the ball this year.”</p>

			<p>Nelson is looking forward to running behind a big crew up front.</p>

			<p>“I just have to follow my blockers and won’t try and do too much,” Nelson said. “I just get a yard at a time and keep going. We have some big guys. That’s nice to have. It makes my job easier.”</p>

			<p>Sauk Centre returns Barhorst and Chalupnik at tackle on the defensive side. Nelson, Wanderscheid and Noah Schmidt all return at linebacker while Will Funk and Moritz return at cornerback. Danny Schmeising and Tanner Schmeising will get the nod at the defensive ends.</p>

			<p>“Nelson will have to lead us on the defensive side, too,” Warring said. “He’s really good at reading and flowing. He’s just a good football player. Those three returning linebackers played extensively last year. We have some experience, which is always good. I think we have some good guys who can step in and make a difference.”</p>

			<p>Warring is hoping his team can compete in the West Central Conference this year against the heavy favorites BOLD and Paynesville.</p>

			<p>“I hope we’re making some noise by the end of the year,” Warring said. “I’d like us to be competing for a conference championship. I think it’s possible.”</p>
