			<p>His receivers will be new. Included are Ethan Novacinski, Cole Johnson, Dillan Bierschbach and Ben Giese at wide receiver and — depending on the formation — tight end Jack Atkinson.</p>

			<p>All are basketball players. Novacinski is 6-foot-6, Johnson is 6-3, Bierschbach is 6-2, Giese is 6-3 and Atkinson is 6-4.</p>

			<p>Maybe the Eagles should be called “Benny and the Tall Jets.”</p>

			<p>Defensively is where Apollo may stand out early as all the new players get used to each other offensively. Ten seniors are expected to start, led by returning linebacker Sean Zyvoloski and defensive linemen Martin Hanson, Swin Sinthavong, Jacob Thralow and Nate Bergherr.</p>

			<p>“I’m very pleased with our defense in general,” Apollo head coach Justin Skaalerud said. “The guys up front are looking good.</p>

			<p>“We’ve got some options there. And our linebackers are very good. Sean and Hunter were all-area type players.”</p>

			<p>The Eagles have 31 seniors and decent depth.</p>

			<p>The coaching staff is working on platooning and limiting two-way play. There are only a couple of players who will be going two ways for sure, Sinthavong and Zyvoloski, though others will rotate in.</p>

			<p>Skaalerud said using a platoon system was something he felt was needed after last season, when Apollo used a lot of two-way players. Big schools like Brainerd tended to wear the Eagles down.</p>

			<p>“Our goal is to platoon and we feel like we’re getting there,” Skaalerud said.</p>

			<p>Apollo hopes all that adds up to another division championship.</p>
