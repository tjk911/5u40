			<p>You could have called the 2013 St. Cloud Apollo Eagles “Benny and the Jets.”</p>

			<p>(Queue up the old-school Elton John here).</p>

			<p>Benny was junior quarterback Ben Alvord, who often would find his two fleet-footed wide receivers, Noel Teigen and Jeremy Koffi, for long touchdowns. Those two were the Jets.</p>

			<p>Teigen (St. John’s University) and Koffi (St. Cloud State) have taken their games to the college level and Alvord will soon follow in 2015, but it doesn’t look like Benny will be a solo act this season.</p>

			<p>Instead of two Jets, Alvord could have as many as six, head coach Justin Skaalerud predicted.</p>

			<p>Four of them are basketball players, a sign that things have changed on the north side, where the Eagles hope to defend their Central Lakes Conference South Division championship.</p>

			<p>Benny and the New Jets are hoping for an encore. (“Saturday Night’s Alright For Fighting” or “Candle In The Wind” perhaps?)</p>

			<p>“We’ve got a lot of guys who have been waiting for this year,” said Alvord, whose season was cut short to six games because of a broken hand. He still completed 72 of 128 passes for 1,306 yards and 13 touchdowns. He rushed for 141 yards and six TDs.</p>

			<p>Alvord has 2,470 career passing yards with 27 TDs, completing 171 of 316 passes.</p>

			<p>A healthy season likely means Alvord winds up as the most prolific passer in school history. He’s getting college looks from small Division I and Division II schools.</p>

