			<p>Paynesville isn’t trying to rest on its laurels.</p>

			<p>But after going 8-0 to win the West Central Conference championship, and finishing 10-1 overall last fall, the Bulldogs have a right to be proud.</p>

			<p>“Being the defending champions puts pressure on you, but we have the players and potential to do it again,” Bulldogs senior tailback/linebacker Alex Kranz said. “If we all do our jobs, we should be a tough team again this year.”</p>

			<p>With stellar senior quarterback Matthew Quade returning, there are high expectations around Paynesville. Quade threw for 1,779 yards and 16 touchdowns last season and had eight interceptions. He also ran for 609 yards and 11 scores.</p>

			<p>“Anytime you bring a guy back with that kind of experience, it puts you three steps ahead of the game at the start of the season,” sixth-year Paynesville head coach Max Meagher said. “As long as he keeps progressing throughout the year, we have that much more potential.”</p>

			<p>The 6-foot-1, 181-pound Quade helped organize a Wednesday night passing league during the summer and attended a passing camp at Southwest Minnesota State.</p>

			<p>“I threw the ball around a lot this summer,” Quade said. “I learned how to become more accurate with my throws. I’ll know how to read a defense better this year and my footwork will be better. We’re a lot farther along than we were at this time last year.”</p>

