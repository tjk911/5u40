			<p>Quade wants to spread the ball around in the passing game. His top targets will be senior wide receivers Nick Dingmann and Mitch Weidner. Dingmann had 27 catches for 454 yards and nine TDs last season while Weidner had 16 catches for 201 yards.</p>

			<p>“I think I can become a better and more accurate passer,” Quade said. “Last year I relied on Andrew Topp and Nick too much. This year I want to spread it around.”</p>

			<p>Paynesville lost four of its five starting offensive lineman from last year. The only returning starter is 6-1, 190-pound guard Anthony Wendlandt.</p>

			<p>The Bulldogs will rely on a three-headed attack out of the backfield with seniors Bryce Savage and Trent Gertken and junior Grant Thompson. Savage ran for 722 yards and six TDs.</p>

			<p>“I’m a pulling guard, so I have to get in front of those guys, otherwise they’ll beat you to the hole,” Wendlandt said. “I like to pull for those guys and we like to go right up the gut. We’re going to run it right down team’s throats if they take the pass away.”</p>

			<p>The Bulldogs return eight starters on defense, with Savage, Gertken and Kranz back at linebacker, Quade, Dingmann and Thompson are back at the safeties, and Weidner and Brandon Schleper return at cornerback.</p>

