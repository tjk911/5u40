			<p>There are a lot of things that are important for a high school team to be successful in sports. One that seems to be becoming increasingly vital is adaptability.</p>

			<p>Leo Pohlkamp is beginning his 29th season as the head coach for Pierz. Even though his team made the state playoffs last season, he decided to make a scheme change this fall.</p>

			<p>Last season, the Pioneers started four linemen and three linebackers on defense. This season, Pierz will go with three linemen and four linebackers.</p>

			<p>“It seems that everybody is doing that and it seems to give you more flexibility in what you can do,” Pohlkamp said. “We’ve got some kids who haven’t played 3-4 and it’s going to be our job to coach them up and get them ready.</p>

			<p>“But these kids seem to be pretty darn smart.”</p>

			<p>If the first few days of practice are any indication, the Pioneers seem to be adjusting quickly.</p>

			<p>“We need to communicate a lot on defense because it’s a lot of new stuff,” said senior Robbie Skiba, who will start at linebacker. “Everybody needs to do their job.”</p>

			<p>Beau Winscher, a senior, is moving from to linebacker after starting at defensive end last season.</p>

			<p>“It’s confusing at first, but everybody’s getting the hang of it,” Winscher said. “We don’t have the size that we did last year.</p>

