			<p>“Toward the end of (last) year, I missed the linebacker position and now I’m back there. It’s kind of a relief to be back where I used to be.”</p>

			<p>Skiba (45 tackles) and Winscher (56 tackles, 5 1/2 sacks) are two of the six returning starters from a defense that gave up an average of 15.3 points last season. The other returning starters are ends Ryan Schmidtbauer (8 tackles) and Austin Gall (57 tackles, 3 1/2 sacks), linebacker Noah Boser (54 tackles, 4 pass deflections), safety Michael Meyer (16 tackles, 3 pass deflections) and cornerback Jonny Kasper (57 tackles, 11 pass deflections).</p>

			<p>The six returning starters on offense are Skiba at running back, Winscher at tight end, quarterback Ethan Lochner, center Dan Poegel, right guard Austin Gall and Schmidtbauer at left guard. Gall started last season at tight end.</p>

			<p>The Pioneers rushed for 3,183 yards (6.3 yards-per-carry average) and 43 touchdowns and averaged 26.8 points last season.</p>

			<p>“Probably the strength of our team is we have a good stable of running backs,” Pohlkamp said.</p>

			<p>Uncharacteristically, the Pioneers finished third in the conference last season. But, despite moving up to Class 3A, Pierz advanced to the state playoffs for the 10th time.</p>

			<p>“It was a big confidence boost and everybody knows what we need to do,” Winscher said. “We want to get back there and get some more.”</p>
