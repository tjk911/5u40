			<p>The way the summer went for Neil Jensen and Brandon Snoberger, you’d have thought they had a full-time travel agent all to themselves.</p>

			<p>Between the two, the Sartell seniors’ busy itineraries included college camps at Iowa, Minnesota, North Dakota, North Dakota State, Illinois State and Winona State. And that doesn’t include an Elite 11 camp in Chicago.</p>

			<p>“They’re all about the same,” said Snoberger, a 6-foot-5, 205-pound senior quarterback who is obviously drawing college interest.</p>

			<p>“It’s all about exposure,” said Jensen, a 6-1, 220-pound senior fullback/linebacker who put some high mileage on his luggage.</p>

			<p>The travel was a little less busy, but the interest is as high for Anthone Thene and Darek Stachowski. Both are healthy after offseason surgery. Thene, the enormous tackle, had work done to his shoulder. Stachowski, the Sabres’ single-season rushing leader, had arthroscopic knee surgery.</p>

			<p>The four are senior captains for the Sabres, who are returning a bus load of experienced players from a squad that ended its 2013 season in the state playoffs.</p>

			<p>“We’ve got some new guys filling spots really well,” said Thene, an offensive tackle listed at 6-3, 350. “Hopefully, they’ll get better and better the more they play.”</p>

			<p>Stachowski, 6-0 and 180, rushed for a school-record 2,155 yards and 21 touchdowns for the Section 8-5A champions.</p>

