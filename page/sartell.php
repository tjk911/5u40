			<p>He’s closing in on the all-time school record of 2,865 yards held by 1995 graduate Tommy Stark. Stark was a St. Cloud State teammate of head coach Scott Hentges.</p>

			<p>Hentges quickly will add this team is more than the captains. This an unusually talented, hard-working group.</p>

			<p>“It’s really nice to have a lot of experience back,” Hentges said. “I think we’re a little ahead of where we usually are and where we’ve been in the past.</p>

			<p>“We took our lumps early last year, played a lot of juniors. But it’s paying off, at least right now.”</p>

			<p>Hentges mentioned that it’s more than just Thene on the line. Right tackle Brandon Fern, he said, has improved immensely and should be a big contributor.</p>

			<p>Hunter Anderson should have a big year opposite of Jensen on the defensive line. Linebackers Daniel Vitale and Payton Voss are coming off strong 2013 seasons. Vitale is an outstanding wrestler.</p>

			<p>Grant Lahn, a former quarterback, should be a big factor at linebacker and will rotate at tight end.</p>

			<p>And it’s not all Stachowski and the running game offensively. Jensen and Snoberger said that Ben Rengel looks like he will have a big year as a receiver. Wide receiver Jake Lieberg, the only junior expected to start on offense, and tight end Tony Deeb also will contribute.</p>
