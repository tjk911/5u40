			<p>After losing a big group of seniors to graduation, a young Royalton Royals team will be relying heavily on their speedy offense and rugged defense this season.</p>

			<p>“We are a ridiculously fast team. We have a tackle that can run a mid-five (40-yard dash), and he is probably one of our slowest guys on offense,” said fourth-year head coach Jamie Morford.</p>

			<p>“I am also putting a huge burden on our defense. We will always be able to score points offensively, but we are really going to put a premium on our defense this season.”</p>

			<p>Even though the defense will be the team’s priority, quarterback Brady Moulzolf is focused on helping his teammates perfect the offensive system.</p>

			<p>“I know most of the offense pretty well,” said the 5-foot-11, 150-pound senior.</p>

			<p>“It’s about getting the new players to figure it out well enough so that we can get the offense perfect.”</p>

			<p>After losing 12 starters to graduation, Royalton will have six returning to this year’s team and the Royals will have to rely on younger players to step up.</p>

			<p>Although new faces will appear on the field this season, the Royals still have their eyes on making a deep run in the playoffs following a heartbreaking loss in the section semifinals last season.</p>

