			<p>“We definitely have our eyes on making it farther this year than we have in the past years,” Moulzolf said. “We have the athletes to do it, we just need to take every game as an important game.”</p>

			<p>Royalton beat New York Mills 47-6 in the first round of the Section 6A playoffs before losing a close, hard-fought game to Browerville, 16-13.</p>

			<p>Even though every game this season will be important to Morford, he believes that his players will be eager to face Browerville once again to try and seek revenge.</p>

			<p>“A game that the kids are definitely circling on the schedule is the Browerville game,” he said. “I think they want some payback, because that one hurt them. That’s a game that if we played it 10 times, we would win it nine of them, but they got the best of us and played a heck of a game.”</p>

			<p>Morford gives a lot of credit Royalton’s baseball team, which made it the state tournament in the spring, for giving some of his younger players some well-needed playoff experience.</p>

			<p>“With that type of understanding to what it takes to get to state, especially as the underdogs, is going to be huge for us,” he said.</p>

			<p>“Those things will hopefully carry over to us being successful in the football season.”</p>

			<p>Returning starter Kyle Billmeyer knows where he wants his team to go and how he needs to step into the leader role for the younger players.</p>

			<p>“If the younger kids are doing something wrong, you just have to show them how to do it right and push them to be better,” said the 5-foot-9, 150-pound senior. “I really want to win the conference, especially after being so close in the loss against Browerville.</p>

			<p>“Our goal is to get to where we were last year, make it even further and play football in November.”</p>
